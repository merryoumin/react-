for (let i = 0; i < 10; i++) {
  let star = "";

  for (let j = 0; j < 10; j++) {
    if (j < i) {
      star += "*";
    }
  }
  console.log(star);
}
//   들어와서 j가 열번 돌아감
// i : 10번 / j: 100번
// i = 0 / j(0) < 0 ->""
// i = 1 / j(0) < 1 -> "*"
// 1 = 2/ j(0,1) < 2 -> "**"
